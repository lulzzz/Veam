using Microsoft.AspNetCore.Hosting;
using Microsoft.AspNetCore.Http;
using System;
using System.Collections.Generic;
using System.IO;
using System.Threading.Tasks;

namespace Veam.Services
{
    public class FileSerivce : IFileService
    {
        public async Task<string> UploadFile(List<IFormFile> files, IHostingEnvironment env,string rootfolderurl,string folder)
        {
            var result = "";

           // var webRoot = env.WebRootPath;
            var uploads = System.IO.Path.Combine(rootfolderurl, folder);
            var extension = "";
            var filePath = "";
            var fileName = "";


            foreach (var formFile in files)
            {
                if (formFile.Length > 0)
                {
                    extension = System.IO.Path.GetExtension(formFile.FileName);
                    fileName = Guid.NewGuid().ToString() + extension;
                    filePath = System.IO.Path.Combine(uploads, fileName);

                    using (var stream = new FileStream(filePath, FileMode.Create))
                    {
                        await formFile.CopyToAsync(stream);
                    }

                    result = fileName;

                }
            }

            return result;
        }
